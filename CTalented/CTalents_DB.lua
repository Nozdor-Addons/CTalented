CustomTalentsSpellData = CustomTalentsSpellData or {}
CustomTalentsServerIds = CustomTalentsServerIds or {}

local function AddClass(classKey, treeDefs)
    CustomTalentsData[classKey] = CustomTalentsData[classKey] or {}
    for specIndex = 0, 8 do
        local spec = CustomTalentsData[classKey][specIndex] or {}
        spec.trees = {}
        for i, d in ipairs(treeDefs) do
            spec.trees[i] = {
                name       = d.name,
                background = d.background,
                icon       = d.icon,
                gridRows   = 11,
                gridCols   = 4,
                talents    = {},
            }
        end
        CustomTalentsData[classKey][specIndex] = spec
    end
end

